//  Operator :-  i. Arithmetic operators :-  + , - , * , / , % 
//               ii. Relational :- = , == , != , < , > , <= , >=
//               iii. Logical :- && , || 
#include<iostream>
using namespace std;
int main()
{
    int  a=2 , b=4;
    cout<<"a + b = "<<a+b;
    cout<<"\n a - b = "<<a-b;
    cout<<"\n a * b = "<<a*b;
    cout<<"\n a / b = "<<a/b;
    cout<<"\n a % b = "<<a%b;
    return 0;
}